const mongoose=require("mongoose")

const User_Schema = new  mongoose.Schema({
    email:{
        type:String,
        required:true,
        unique:true,
        trim:true,
        lowercase:true,
    },

    password:
    {
        type:String,
        required:true,
        minlength:6
    }
})

const User= new mongoose.model("User",User_Schema)

module.exports=User;